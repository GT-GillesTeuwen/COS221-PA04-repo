package DataObjects;

/**
 *
 * @author gteuw
 */
public class Language {
    int id;
    String name;

    public Language(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name;
    }
    
    
    
    
}
